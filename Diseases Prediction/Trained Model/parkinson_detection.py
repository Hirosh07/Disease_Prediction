parkin_data.isnull().sum()

"""1 ---> Positive,
0 ---> Negative
"""

parkin_data['status'].value_counts()

parkin_data.groupby('status').mean()

X = parkin_data.drop(columns=['name', 'status'],axis=1)
Y = parkin_data['status']

X

Y

x_train, x_test, y_train, y_test = train_test_split(X, Y, test_size=0.2, random_state=2)
print(X.shape, x_train.shape, x_test.shape)

scaler = StandardScaler()
scaler.fit(x_train)

x_train = scaler.transform(x_train)

x_test = scaler.transform(x_test)

print(x_train)

"""Support Vector Machine Model

"""

model = svm.SVC(kernel='linear')

model.fit(x_train, y_train)

x_train_prediction = model.predict(x_train)
training_data_acc = accuracy_score(y_train, x_train_prediction)

print('Accuracy of train data is ', training_data_acc)

x_test_prediction = model.predict(x_test)
test_data_acc = accuracy_score(y_test, x_test_prediction)

print('Accuracy of test is ', test_data_acc)

